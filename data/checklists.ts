export function getAllChecklists(): Checklist[] {
  return categories.flatMap((cat) => cat.checklists);
}

export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((cat) => cat.slug === slug);
}

export function getChecklistBySlug(categorySlug: string, checklistSlug: string): Checklist | undefined {
  const category = getCategoryBySlug(categorySlug);
  return category?.checklists.find((cl) => cl.slug === checklistSlug);
}

export function searchChecklists(query: string): Checklist[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return getAllChecklists().filter(
    (cl) =>
      cl.title.toLowerCase().includes(q) ||
      cl.description.toLowerCase().includes(q)
  );
}
